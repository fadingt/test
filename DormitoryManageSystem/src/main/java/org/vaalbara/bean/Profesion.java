public class Profesion {
    private int pId;
    private String pName;

    @Override
    public String toString() {
        return "Profesion{" +
                "pId=" + pId +
                ", pName='" + pName + '\'' +
                '}';
    }

    public Profesion() {
    }

    public Profesion(int pId, String pName) {
        this.pId = pId;
        this.pName = pName;
    }

    public int getpId() {
        return pId;
    }

    public void setpId(int pId) {
        this.pId = pId;
    }

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }
}
