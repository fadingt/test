package org.vaalbara.bean;

/**
 * Created by Huawei on 2018/3/16.
 */
public class StudentInfoMessage {
    private int sId;        //学号
    private String sName;   //姓名
    private String pName;   //专业名
    private String sClass;  //年级
    private int  dId;       //宿舍号
    private String bedId;   //床位
    private int forYear;    //度过年数

    private String sEmail;  //学生邮箱
    private String sPhone;  //学生电话

    private int vCount;     //请假次数
    private int rCount;     //报修次数
    private int cCount;     //处分次数


    public StudentInfoMessage() {
    }

    public StudentInfoMessage(String sName, String pName, String sClass, int dId, String bedId, int forYear, String sEmail, String sPhone, int vCount, int rCount, int cCount) {
        this.sName = sName;
        this.pName = pName;
        this.sClass = sClass;
        this.dId = dId;
        this.bedId = bedId;
        this.forYear = forYear;
        this.sEmail = sEmail;
        this.sPhone = sPhone;
        this.vCount = vCount;
        this.rCount = rCount;
        this.cCount = cCount;
    }

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }

    public String getsClass() {
        return sClass;
    }

    public void setsClass(String sClass) {
        this.sClass = sClass;
    }

    public int getdId() {
        return dId;
    }

    public void setdId(int dId) {
        this.dId = dId;
    }

    public String getBedId() {
        return bedId;
    }

    public void setBedId(String bedId) {
        this.bedId = bedId;
    }

    public int getForYear() {
        return forYear;
    }

    public void setForYear(int forYear) {
        this.forYear = forYear;
    }

    public String getsEmail() {
        return sEmail;
    }

    public void setsEmail(String sEmail) {
        this.sEmail = sEmail;
    }

    public String getsPhone() {
        return sPhone;
    }

    public void setsPhone(String sPhone) {
        this.sPhone = sPhone;
    }

    public int getvCount() {
        return vCount;
    }

    public void setvCount(int vCount) {
        this.vCount = vCount;
    }

    public int getrCount() {
        return rCount;
    }

    public void setrCount(int rCount) {
        this.rCount = rCount;
    }

    public int getcCount() {
        return cCount;
    }

    public void setcCount(int cCount) {
        this.cCount = cCount;
    }


    @Override
    public String toString() {
        return "StudentInfoMessage{" +
                "sName='" + sName + '\'' +
                ", pName='" + pName + '\'' +
                ", sClass='" + sClass + '\'' +
                ", dId=" + dId +
                ", bedId='" + bedId + '\'' +
                ", forYear=" + forYear +
                ", sEmail='" + sEmail + '\'' +
                ", sPhone='" + sPhone + '\'' +
                ", vCount=" + vCount +
                ", rCount=" + rCount +
                ", cCount=" + cCount +
                '}';
    }
}
