public class DroStudent {
    private Integer dId;//宿舍ID
    private Integer sId;//学生ID
    private Integer bedId;//床ID

    public DroStudent() {
    }

    public Integer getdId() {
        return dId;
    }

    public void setdId(Integer dId) {
        this.dId = dId;
    }

    public Integer getsId() {
        return sId;
    }

    public void setsId(Integer sId) {
        this.sId = sId;
    }

    public Integer getBedId() {
        return bedId;
    }

    public void setBedId(Integer bedId) {
        this.bedId = bedId;
    }

    public DroStudent(Integer dId, Integer sId, Integer bedId) {
        this.dId = dId;
        this.sId = sId;
        this.bedId = bedId;
    }

    public DroStudent(Integer sId, Integer bedId) {
        this.sId = sId;
        this.bedId = bedId;
    }



    @Override
    public String toString() {
        return "DroStudent{" +
                "dId=" + dId +
                ", sId=" + sId +
                ", bedId=" + bedId +
                '}';
    }
}
