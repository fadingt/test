public class Links {
        private int linksId;     //公告id
        private String linksName;   //公告标题
        private String linksUrl;    //公告摘要
        private String masterEmail; //发布人  管理员名
        private String linksTime;   //发布时间
        private String showAddress = "首页"; //展示位置


    public Links() {
    }

    public Links(int linksId, String linksName, String linksUrl, String masterEmail, String linksTime, String showAddress) {
        this.linksId = linksId;
        this.linksName = linksName;
        this.linksUrl = linksUrl;
        this.masterEmail = masterEmail;
        this.linksTime = linksTime;
        this.showAddress = showAddress;
    }

    public Links(int linksId, String linksName, String linksUrl, String masterEmail, String linksTime) {
        this.linksId = linksId;
        this.linksName = linksName;
        this.linksUrl = linksUrl;
        this.masterEmail = masterEmail;
        this.linksTime = linksTime;
    }

    @Override
    public String toString() {
        return "Links{" +
                "linksId='" + linksId + '\'' +
                ", linksName='" + linksName + '\'' +
                ", linksUrl='" + linksUrl + '\'' +
                ", masterEmail='" + masterEmail + '\'' +
                ", linksTime='" + linksTime + '\'' +
                ", showAddress='" + showAddress + '\'' +
                '}';
    }

    public int getLinksId() {
        return linksId;
    }

    public void setLinksId(int linksId) {
        this.linksId = linksId;
    }

    public String getLinksName() {
        return linksName;
    }

    public void setLinksName(String linksName) {
        this.linksName = linksName;
    }

    public String getLinksUrl() {
        return linksUrl;
    }

    public void setLinksUrl(String linksUrl) {
        this.linksUrl = linksUrl;
    }

    public String getMasterEmail() {
        return masterEmail;
    }

    public void setMasterEmail(String masterEmail) {
        this.masterEmail = masterEmail;
    }

    public String getLinksTime() {
        return linksTime;
    }

    public void setLinksTime(String linksTime) {
        this.linksTime = linksTime;
    }

    public String getShowAddress() {
        return showAddress;
    }

    public void setShowAddress(String showAddress) {
        this.showAddress = showAddress;
    }
}
