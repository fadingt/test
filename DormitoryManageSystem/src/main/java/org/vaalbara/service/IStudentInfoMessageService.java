public interface IStudentInfoMessageService {

    /**
     * 获取所有所要展示的信息
     * @param sId
     * @return
     */
    StudentInfoMessage getAllMessage(int sId);



    /**
     * 根据id获取请假次数
     * @param sId
     * @return
     */
    int getVocationCount(int sId);

    /**
     * 根据id获取报修次数
     * @param sId
     * @return
     */
    int getRepairCount(int sId);

    /**
     * 根据id获取处分次数
     * @param sId
     * @return
     */
    int getCriticizeCount(int sId);
}
